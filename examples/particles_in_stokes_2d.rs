struct Args {
    steps:     usize,
    out_every: usize,
    dt:        f64,
    particles: usize,
    seed:      u64,
}

fn parse_args() -> Args {
    let raw: Vec<String> = std::env::args().collect();
    let mut steps     = 100usize;
    let mut out_every = 10usize;
    let mut dt        = 0.1f64;
    let mut particles = 4usize;
    let mut seed      = 42u64;
    let mut i = 1;
    while i < raw.len() {
        match raw[i].as_str() {
            "--steps"     => { steps     = raw[i+1].parse().unwrap(); i += 2; }
            "--out-every" => { out_every = raw[i+1].parse().unwrap(); i += 2; }
            "--dt"        => { dt        = raw[i+1].parse().unwrap(); i += 2; }
            "--particles" => { particles = raw[i+1].parse().unwrap(); i += 2; }
            "--seed"      => { seed      = raw[i+1].parse().unwrap(); i += 2; }
            _ => { i += 1; }
        }
    }
    Args { steps, out_every, dt, particles, seed }
}

// ------------------------------------------------------------
// Minimal xorshift64 PRNG (no external crate needed)
// ------------------------------------------------------------

struct Rng(u64);

impl Rng {
    fn new(seed: u64) -> Self { Self(seed.max(1)) }

    fn next_u64(&mut self) -> u64 {
        self.0 ^= self.0 << 13;
        self.0 ^= self.0 >> 7;
        self.0 ^= self.0 << 17;
        self.0
    }

    /// Uniform f64 in [0, 1)
    fn next_f64(&mut self) -> f64 {
        (self.next_u64() >> 11) as f64 / (1u64 << 53) as f64
    }

    /// Uniform f64 in [lo, hi)
    fn uniform(&mut self, lo: f64, hi: f64) -> f64 {
        lo + self.next_f64() * (hi - lo)
    }
}

